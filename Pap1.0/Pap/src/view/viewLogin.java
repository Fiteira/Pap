    private void jButtonLoginFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginFuncionarioActionPerformed

        loginDAO dao = new loginDAO();

        if(dao.checkLogin(txtLoginFuncionario.getText(), txtPassFuncionario.getText())){
            new viewEmenta().setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Senha incorreta!");
        }

    }//GEN-LAST:event_jButtonLoginFuncionarioActionPerformed

