    private void txtPassFuncionarioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPassFuncionarioKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            loginDAO dao = new loginDAO();

            if(dao.checkLogin(txtLoginFuncionario.getText(), txtPassFuncionario.getText())){
                new viewEmenta().setVisible(true);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Senha incorreta!");
            }
        }
    }//GEN-LAST:event_txtPassFuncionarioKeyPressed

